import streamlit as st
import google.generativeai as genai

# 1. إعداد واجهة المستخدم
st.set_page_config(page_title="حصانة الرقمية - RegHasana", layout="wide")
st.title("🛡️ منصة حصانة الرقمية (RegHasana)")
st.subheader("مساعد الامتثال الذكي المعتمد على تشريعات SAMA و CMA")

# 2. إدخال مفتاح الـ API الخاص بـ Gemini (يمكنك الحصول عليه مجاناً من Google AI Studio)
# للمحاكاة السريعة في الهاكاثون يمكنك وضعه مباشرة هنا أو تركه كإدخال للمستخدم
api_key = st.sidebar.text_input("أدخل مفتاح Gemini API Engine:", type="password")

# 3. محاكاة قاعدة البيانات (البيانات النصية والتشريعات الموفرة)
SAMA_CMA_REGULATIONS = """
[مادة 1: توثيق هوية العميل KYC في التمويل الجماعي]
يجب على كافة شركات التقنية المالية المرخصة من البنك المركزي السعودي (SAMA) أو هيئة السوق المالية (CMA) التحقق من هوية العميل عبر نظام "أبشر" أو "نفاذ" الوطني الموحد قبل إتمام أي عملية تمويل أو استثمار قائم على الصكوك. يمنع قبول أي عميل بدون ربط آلي مباشر.

[مادة 2: مكافحة غسل الأموال AML]
يلزم الإبلاغ الفوري عن أي عملية مالية تتجاوز قيمتها 50,000 ريال سعودي إذا كانت قادمة من حساب حديث الإنشاء أو لا تتطابق مع السلوك الاستثماري المعتاد للمستخدم، وتجميد الحساب احترازياً لمدة 24 ساعة.
"""

# 4. تفعيل نموذج الذكاء الاصطناعي
if api_key:
    genai.configure(api_key=api_key)
    model = genai.GenerativeModel('gemini-pro')
else:
    st.warning("رجاءً أدخل مفتاح الـ API في الشريط الجانبي لتشغيل مساعد الذكاء الاصطناعي.")

# تقسيم الشاشة إلى قسمين (لوحة التحكم والمساعد الذكي)
col1, col2 = st.columns([1, 1])

with col1:
    st.header("🔍 فاحص العقود والامتثال الآلي")
    uploaded_file = st.text_area("أدخل نص العقد أو سياسة الخصوصية للشركة الناشئة هنا لمطابقتها:", height=200, 
                                 value="مثال لعقد: نحن شركة تمويل جماعي، نقوم بتوثيق هوية العملاء عن طريق رفع صورة الهوية يدوياً في الموقع دون ربط بنظام نفاذ نظراً لتأخر الإجراءات...")
    
    if st.button("افحص الامتثال فوراً"):
        if not api_key:
            st.error("يرجى إدخال المفتاح أولاً.")
        else:
            with st.spinner("جاري مسح العقد ومطابقته باللوائح..."):
                # هنا نقوم بعمل تقنية RAG مبسطة (دمج سياق اللوائح مع سؤال أو عقد المستخدم وتمريرها للـ LLM)
                prompt = f"""
                أنت مستشار قانوني خبير في تشريعات FinTech السعودية (SAMA و CMA). 
                بناءً على اللوائح التالية فقط:
                {SAMA_CMA_REGULATIONS}
                
                قم بتحليل النص التالي المستخرج من نظام الشركة، وحدد المخالفات بدقة، واقترح الصياغة القانونية البديلة المصححة 100%:
                النص المراد فحصه: {uploaded_file}
                """
                response = model.generate_content(prompt)
                st.success("تم الفحص بنجاح!")
                st.markdown("### 🚨 النتيجة والتصحيح الآلي:")
                st.write(response.text)

with col2:
    st.header("💬 مستشار الامتثال الذكي (Copilot)")
    user_query = st.text_input("اسأل المساعد القانوني عن أي تشريع (مثال: ما هي شروط توثيق العميل؟):")
    
    if user_query:
        if not api_key:
            st.error("يرجى إدخال المفتاح أولاً.")
        else:
            with st.spinner("جاري استخراج التشريع والإجابة..."):
                prompt = f"""
                بناءً على اللوائح والتعاميم المحدثة لديك:
                {SAMA_CMA_REGULATIONS}
                
                أجب على سؤال المستخدم التالي باللغة العربية الفصحى وبأسلوب مهني مدعماً بالمواد القانونية:
                السؤال: {user_query}
                """
                response = model.generate_content(prompt)
                st.markdown("### 🤖 إجابة المستشار الافتراضي:")
                st.write(response.text)